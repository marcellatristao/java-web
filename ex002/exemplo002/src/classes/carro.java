package classes;

public class carro {

	//Atributos do carro
	String placa;
	String cor;
	String valor;
	
	
	
	//Construtor
	public void carro() {
		
	}
	
	
	//Getters and setters
	public String getPlaca() {
		return placa;
	}

	public void setPlaca(String placa) {
		this.placa = placa;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}
	
}
